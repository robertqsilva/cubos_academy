let x = 20, y = 10;


//soma
let resultado = x + y;
console.log(`A soma de ${x} e ${y} é ${resultado}`);//30

//subtração
resultado = x - y;
console.log(`A subtração de ${x} e ${y} é ${resultado}`);//10


//multiplicação
resultado = x * y;
console.log(`A multiplicação de ${x} e ${y} é ${resultado}`);//200

//divisão
resultado = x / y;
console.log(`A divisão de ${x} e ${y} é ${resultado}`); //2